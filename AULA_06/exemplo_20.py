idades = [20,10,15,30,50,12,60,22,55,35]
print(f"A soma das Idade é: {sum(idades)} anos.") #sum- soma
print(f"A maior Idade é: {max(idades)} anos.") #max- maior
print(f"A menor Idade é: {min(idades)} anos.") #min- menor
print(f"A média das Idades é: {sum(idades)/ len(idades)} anos.")
idades.sort() #usar para ser crescente
print(idades)